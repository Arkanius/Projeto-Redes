      private boolean checkPassword (char[] password){
        boolean check = true;
         char [] correctPassword = {'f','a','t','e','c'}; // Array de char contendo senha correta
         
         if(password.length != correctPassword.length){ //Verifica se o tamanho da senha digitada pelo usuário é diferente da senha correta
             check = false;
         }else{
             check = Arrays.equals(correctPassword,password); //Arrays.equals compara valores de 2 arrays, retorna true para igual e false para diferente
         }
       
         
         
          Arrays.fill(correctPassword,'0'); //Preenche a variavel com 0 por razões de segurança     
          
          
          return check; //Retorna true para senha correta ou falso para senha incorreta
       }
    
    
